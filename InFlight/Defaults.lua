	for faction, t in pairs(data) do
		temp[faction] = {}
		for source, dt in pairs(t) do
			local sl = localization[source]
			if sl then
				temp[faction][sl] = {}
				for destination, time in pairs(dt) do
					local dl = localization[destination]
					if dl then
						temp[faction][sl][dl] = time
					end
				end
			end
		end
	end
	return temp
